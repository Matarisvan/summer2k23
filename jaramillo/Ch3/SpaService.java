package jaramillo.Ch3;

public class SpaService {
    
    // private fields
    private String serviceDesc;
    private double price;

    // get set methods
    public String getServiceDesc() {
        return serviceDesc;
    }
    public void setServiceDesc(String setServDesc) {
        serviceDesc = setServDesc;
    }
    public double getPrice() {
        return price;
    }
    public void setPrice(double setPrice) {
        price = setPrice;
    }
    
}
